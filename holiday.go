func GetHolidayDescription(date time.Time) (string, error) {
	for _, holiday := range holidays {
		if holiday.year == date.Year() && holiday.month == int(date.Month()) && holiday.day == date.Day() {
			return holiday.description, nil
		}
	}
	return "", errors.New(date.Format("2006-01-02") + " is not holiday")
}

func MaxYear() int {
	max := 0
	for _, holiday := range holidays {
		if max < holiday.year {
			max = holiday.year
		}
	}
	return max
}

func MinYear() int {
	min := 100000
	for _, holiday := range holidays {
		if holiday.year < min {
			min = holiday.year
		}
	}
	return min
}

